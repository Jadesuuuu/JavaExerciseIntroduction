package bonifacio_L17Activity1;

public class Song {
	String songTitle;
	String artist;
	
	public Song(String songTitle, String artist) {
		this.songTitle = songTitle;
		this.artist = artist;
	}
	
	public String getSongTitle() {
		return songTitle;
	}

	public String getArtist() {
		return artist;
	}
	
}
