package JavaExercise1;

public class BartDetention {
	public static void main(String[] args) {
		for (int i = 0; i < 6; i++) {
			printParagraph();
		}
	}
	
	private static void printParagraph() {
		System.out.println("I will not copy and paste code.\r\n"
				+ "I will use Camel Case when writing class names.\r\n"
				+ "I will use lower Camel Case when writing function names.\r\n"
				+ "I will use lower Camel Case when writing variables names.");
		System.out.println();
	}
	
}
